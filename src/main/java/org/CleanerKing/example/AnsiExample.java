package org.CleanerKing.example;

public class AnsiExample {
    public static void main(String[] args) {
        System.out.println("\u001B[31m这是红色文本\u001B[0m");  // 红色
        System.out.println("\u001B[32m这是绿色文本\u001B[0m");  // 绿色
        System.out.println("\u001B[33m这是黄色文本\u001B[0m");  // 黄色
        System.out.println("\u001B[34m这是蓝色文本\u001B[0m");  // 蓝色
    }
}
